struct SkiFriction {
	int bestPosition(string sf, string pf) {
		int rez=0;
		for(int i=0; i<pf.size()-sf.size(); ++i) {
			int m=0;
			for(int j=0; j<sf.size(); ++j) m=max(m,sf[j]-'0'+pf[i+j]-'0');
			rez+=m;
		}
		return rez;
	}
};


double test0() {
	string p0 = "45";
	string p1 = "15196";
	SkiFriction * obj = new SkiFriction();
	clock_t start = clock();
	int my_answer = obj->bestPosition(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 33;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
