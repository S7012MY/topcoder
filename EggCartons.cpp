struct EggCartons {
	int minCartons(int n) {
		int rez=9999;
		for(int i=0; i<=n; ++i) {
			int cnt=i*6;
			if(cnt>n) break;
			int ram=n-cnt;
			if(ram%8) continue;
			//cout<<i<<' '<<ram;
			rez=min(rez,i+ram/8);
		}
		if(rez==9999) return -1;
		return rez;
	}
};


double test0() {
	int p0 = 20;
	EggCartons * obj = new EggCartons();
	clock_t start = clock();
	int my_answer = obj->minCartons(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 3;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
