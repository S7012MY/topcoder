struct SoldierLabeling {
	int count(int n, int lowerBound, int upperBound) {
		int first=10,last=10;
		for(int i=2;i<lowerBound; ++i) first*=10;
		for(int i=2; i<=upperBound; ++i) last*=10;
		--last;
		if(first>n) return 0;
		if(last>n) last=n;
		return last-first+1;
	}
};


double test0() {
	int p0 = 100;
	int p1 = 2;
	int p2 = 2;
	SoldierLabeling * obj = new SoldierLabeling();
	clock_t start = clock();
	int my_answer = obj->count(p0, p1, p2);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p3 = 90;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p3 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p3 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
