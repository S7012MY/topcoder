struct BouncingBalls {
	double expectedBounces(vector <int> x, int T) {
		double rez=0;
		int n=x.size();
		sort(x.begin(),x.end());
		for(int msk=0; msk<(1<<n); ++msk) for(int i=0; i<n; ++i)
			for(int j=i+1; j<n; ++j) if((msk&(1<<i)) && !(msk&(1<<j)) && x[i]+T>=x[j]-T)
				++rez;
		return rez/double(1<<n);
	}
};


double test0() {
	int t0[] = {5, 8};
	vector <int> p0(t0, t0+sizeof(t0)/sizeof(int));
	int p1 = 2;
	BouncingBalls * obj = new BouncingBalls();
	clock_t start = clock();
	double my_answer = obj->expectedBounces(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	double p2 = 0.25;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
