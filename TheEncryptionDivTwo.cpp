char lit[30],fl='a';

struct TheEncryptionDivTwo {
	string encrypt(string m) {
		string rez;
		for(int i=0; i<m.size(); ++i) {
			if(0==lit[m[i]]) {
				lit[m[i]]=fl;
				++fl;
			}
			rez+=lit[m[i]];
		}
		return rez;
	}
};


double test0() {
	string p0 = "hello";
	TheEncryptionDivTwo * obj = new TheEncryptionDivTwo();
	clock_t start = clock();
	string my_answer = obj->encrypt(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "abccd";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
