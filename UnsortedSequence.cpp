struct UnsortedSequence {
	vector <int> getUnsorted(vector <int> s) {
		sort(s.begin(),s.end());
		vector<int> rez;
		int n=s.size();
		if(0==n) return rez;
		int ok=1;
		for(int i=1; i<n; ++i) if(s[i]!=s[i-1]) ok=0;
		if(ok) return rez;
		//for(int i=0; i<n; ++i) cout<<s[i]<<' ';
		int i,lp;
		for(lp=s.size()-1; lp>=0 && s[lp]==s[lp-1];--lp);
		for(i=s.size()-2; i>=0; --i) if(s[i]!=s[lp]) {
		swap(s[i],s[lp]);
		break;
		//cout<<"aici";
		}
		
		return s;
	}
};


