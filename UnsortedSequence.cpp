	int t0[] = {1,2};
	vector <int> p0(t0, t0+sizeof(t0)/sizeof(int));
	UnsortedSequence * obj = new UnsortedSequence();
	clock_t start = clock();
	vector <int> my_answer = obj->getUnsorted(p0);
	clock_t end = clock();
