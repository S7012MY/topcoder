struct BaseConfusion {
	LL gauss(LL n) {
		return (n*(n+1))/2LL;
	}
	LL sum(LL a, LL b) {
		return gauss(b)-gauss(a-1);
	}
	
	long long sum(int M, int N, int B) {
		long long rez=0,b=1,sm;
		sm=sum(M,N);
		for(;sm;) {
			rez+=(sm%10)*b;
			b*=(B+1);
			sm/=10;
		}
		return rez;
	}
};


double test0() {
	int p0 = 14;
	int p1 = 18;
	int p2 = 3;
	BaseConfusion * obj = new BaseConfusion();
	clock_t start = clock();
	long long my_answer = obj->sum(p0, p1, p2);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	long long p3 = 129LL;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p3 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p3 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
