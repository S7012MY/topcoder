struct OneDigitDifference {
	int getSmallest(int N) {
		if(N==0) return 1;
		int rez=0,cn=N,nr[50]={0},nc=0,pz=1;
		for(;cn!=0;nr[++nr[0]]=cn%10,cn/=10,++nc);
		for(int i=1; i<nc; ++i) pz*=10;
		//cout<<pz<<' ';
		return N%pz;
	}
};


double test0() {
	int p0 = 9;
	OneDigitDifference * obj = new OneDigitDifference();
	clock_t start = clock();
	int my_answer = obj->getSmallest(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 0;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
