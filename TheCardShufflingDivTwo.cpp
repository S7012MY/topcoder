struct TheCardShufflingDivTwo {
	int shuffle(int n, int m) {
		int rez=1;
		vector<int> st,dr,a;
		for(int i=1; i<=n; ++i)
			if(i&1) dr.push_back(i);
			else st.push_back(i);
		for(int i=0; i<st.size(); ++i) a.push_back(st[i]);
		for(int i=0; i<dr.size(); ++i) a.push_back(dr[i]);
		for(int i=0; i<m; ++i) rez=a[rez-1];
		return rez;
	}
};


double test0() {
	int p0 = 5;
	int p1 = 1;
	TheCardShufflingDivTwo * obj = new TheCardShufflingDivTwo();
	clock_t start = clock();
	int my_answer = obj->shuffle(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
