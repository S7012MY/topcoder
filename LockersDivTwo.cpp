bitset<10005> un;

struct LockersDivTwo {
	int lastOpened(int N) {
		int rez,i=2;
		for(;;++i) {
			int fp=1;
			for(int j=1; j<=N; ++j) if(0==un[j]) {
				fp=j;
				break;
			}
			for(int j=fp; j<=N; j+=i) {
				if(0==un[j]) rez=j;
				un[j]=1;
			}
			if(un.count()==N) break;
		}
		return rez;
	}
};


double test0() {
	int p0 = 9;
	LockersDivTwo * obj = new LockersDivTwo();
	clock_t start = clock();
	int my_answer = obj->lastOpened(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 6;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
