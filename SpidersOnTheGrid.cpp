int mat[55][55];

struct SpidersOnTheGrid {
	int find(vector <string> A) {
		int rez=0,n=A.size(),m=A[0].size();
		for(int i=0; i<n; ++i) for(int j=0; j<m; ++j)
			if(A[i][j]=='N' && i-1>=0) mat[i-1][j]=1;
			else if(A[i][j]=='S' && i+1<n) mat[i+1][j]=1;
			else if(A[i][j]=='E' && j+1<m) mat[i][j+1]=1;
			else if(A[i][j]=='W' && j-1>=0) mat[i][j-1]=1;
		for(int i=0; i<n; ++i) for(int j=0; j<m; ++j) if(!mat[i][j]) ++rez;
		return rez;
	}
};


double test0() {
	string t0[] = {"EW","NN"};
	vector <string> p0(t0, t0+sizeof(t0)/sizeof(string));
	SpidersOnTheGrid * obj = new SpidersOnTheGrid();
	clock_t start = clock();
	int my_answer = obj->find(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
