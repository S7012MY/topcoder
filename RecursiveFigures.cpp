#define PI 3.14159265358
#define SQ2 1.41421356237
struct RecursiveFigures {
	double getArea(int sideLength, int K) {
		double rez=0;
		double sl=sideLength;
		for(int i=1; i<=K; ++i) {
			sl/=2;
			double ac=sl*sl*PI;
			sl=sl*SQ2;
			rez+=ac-sl*sl;
			if(i==K) rez+=sl*sl;
		}
		return rez;
	}
};


double test0() {
	int p0 = 10;
	int p1 = 1;
	RecursiveFigures * obj = new RecursiveFigures();
	clock_t start = clock();
	double my_answer = obj->getArea(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	double p2 = 78.53981633974483;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
