struct MicrowaveSelling {
	int mostAttractivePrice(int minPrice, int maxPrice) {
		int rez,br=0;
		for(int i=minPrice; i<=maxPrice; ++i) {
			int ci=i,nrn=0;
			for(;ci && ci%10==9;ci/=10,++nrn);
			if(nrn>=br) {
				br=nrn;
				rez=i;
			}
		}
		return rez;
	}
};


double test0() {
	int p0 = 460;
	int p1 = 680;
	MicrowaveSelling * obj = new MicrowaveSelling();
	clock_t start = clock();
	int my_answer = obj->mostAttractivePrice(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 599;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
