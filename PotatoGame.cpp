struct PotatoGame {
	string theWinner(int n) {
		//string rez;
		int mod=(n%4);
		if(mod==1 || mod==0) return "Taro";
		else if(mod==2) return "Hanako";
		else if(mod==3) return "Taro";
		return "Hanako";
	}
};


double test0() {
	int p0 = 1;
	PotatoGame * obj = new PotatoGame();
	clock_t start = clock();
	string my_answer = obj->theWinner(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "Taro";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
