struct BunnyExamAfter {
	int getMaximum(string black, string gray, string white) {
		int rez=0,n=black.size();
		for(int i=0; i<n; ++i) {
			if(gray[i]==white[i] && white[i]!=black[i]) rez+=2;
			else if((gray[i]!=white[i]) && (white[i]!=black[i] || gray[i]!=black[i]) ) ++rez;
		}
		return rez;
	}
};


double test0() {
	string p0 = "ABC";
	string p1 = "ABC";
	string p2 = "ABC";
	BunnyExamAfter * obj = new BunnyExamAfter();
	clock_t start = clock();
	int my_answer = obj->getMaximum(p0, p1, p2);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p3 = 0;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p3 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p3 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
