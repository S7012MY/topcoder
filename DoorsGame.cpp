int fol[300];

struct DoorsGame {
	int determineOutcome(string doors, int trophy) {
		int n=doors.size();
		for(int i=0; i<trophy; ++i) fol[doors[i]]|=1;
		for(int i=trophy; i<n; ++i) fol[doors[i]]|=2;
		int ju,gu,co;
		ju=gu=co=0;
		for(char i='A'; i<='P'; ++i)
			if(1==fol[i]) ++ju;
			else if(2==fol[i])++gu;
			else if(3==fol[i]) ++co;
		cout<<ju<<' '<<gu<<' '<<co;
		if(ju+co<=gu) return 2*(ju+co)-1;
		if(gu+co<ju) return -2*(gu+co);
		return 0;
	}
};


double test0() {
	string p0 = "ABCD";
	int p1 = 2;
	DoorsGame * obj = new DoorsGame();
	clock_t start = clock();
	int my_answer = obj->determineOutcome(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 3;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
