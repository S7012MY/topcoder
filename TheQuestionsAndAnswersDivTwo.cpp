struct TheQuestionsAndAnswersDivTwo {
	int find(vector <string> questions) {
		set<string> s;
		for(int i=0; i<questions.size(); ++i) s.insert(questions[i]);
		return (1<<(s.size()));
	}
};


double test0() {
	string t0[] = {"How_are_you_doing?", "How_do_you_like_our_country?", "How_are_you_doing?"};
	vector <string> p0(t0, t0+sizeof(t0)/sizeof(string));
	TheQuestionsAndAnswersDivTwo * obj = new TheQuestionsAndAnswersDivTwo();
	clock_t start = clock();
	int my_answer = obj->find(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 4;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
