struct TheMoviesLevelTwoDivTwo {
	vector <int> find(vector <int> length, vector <int> scary) {
		vector <int> v,r;
		int rez=-1;
		for(int i=0; i<length.size(); ++i) v.push_back(i);
		do {
			double sl=74;
			int cnt=0;
			for(int i=0; i<v.size(); ++i) {
				int ii=v[i];
				sl-=scary[ii];
				if(sl<0) break;
				sl+=47;
				sl-=(length[ii]-scary[ii]);
				if(sl<0) break;
				++cnt;
			}
			if(cnt>rez) {
				rez=cnt;
				r=v;
			}
		}while(next_permutation(v.begin(),v.end()));
		return r;
	}
};


