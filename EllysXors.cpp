struct EllysXors {
//cacat periodic
	LL ux(LL x) {
		if ((x&3LL)==0) return x;
		if ((x&3LL)==1) return 1;
		if ((x&3LL)==2) return x+1;
		return 0;
	}

	long long getXor(long long L, long long R) {
		return ux(R)^ux(L-1);
	}
};


