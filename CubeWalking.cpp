const int dx[4]={0,1,0,-1}, dy[4]={1,0,-1,0};
const char* plane[3][3] = {
    {"RED", "BLUE" ,"RED"},
    {"BLUE","GREEN","BLUE"},
    {"RED", "BLUE" ,"RED"}  };

struct CubeWalking {
	string finalPosition(string movement) {
		int d=0,x=1,y=1;
		for(int i=0; i<movement.size(); ++i)
			if(movement[i]=='L') d=(d+1)%4;
			else if(movement[i]=='R') d=(d+3)%4;
			else {
				x=(x+dx[d]+4)%4;
				y=(y+dy[d]+4)%4;
			}
		return plane[x][y];
	}
};


double test0() {
	string p0 = "LLRR";
	CubeWalking * obj = new CubeWalking();
	clock_t start = clock();
	string my_answer = obj->finalPosition(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "GREEN";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
