#define LL long long
#define ZN 1000000000

set<LL> viz;
struct OneRegister {
	string getProgram(int s, int t) {
		string rez=":-(";
		queue<long long> q;
		queue<string> qs;
		q.push(s);
		qs.push("");
		for(;q.size();q.pop(),qs.pop()) {
			LL k=q.front();
			string fr=qs.front();
			if(k==t) return fr;
			if(k*k<=ZN && viz.find(k*k)==viz.end()) {
				q.push(k*k);
				qs.push(fr+'*');
				viz.insert(k*k);
			}
			if(k*2<=ZN && viz.find(2LL*k)==viz.end()) {
				q.push(k*2);
				qs.push(fr+'+');
				viz.insert(k*2);
			}
			if(viz.find(1)==viz.end()) {
				q.push(1);
				qs.push(fr+'/');
				viz.insert(1);
			}
		}
		return rez;
	}
};


double test0() {
	int p0 = 7;
	int p1 = 392;
	OneRegister * obj = new OneRegister();
	clock_t start = clock();
	string my_answer = obj->getProgram(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p2 = "+*+";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p2 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
