struct MinimalDifference {
	int sd(int n) {
		int r=0;
		for(;n;r+=(n%10),n/=10);
		return r;
	}
	int findNumber(int A, int B, int C) {
		int rez=9999999,ret;
		for(int i=A; i<=B; ++i) {
			if(abs(sd(i)-sd(C))<rez) {
				rez=abs(sd(i)-sd(C));
				ret=i;
			}
		}
		return ret;
	}
};


double test0() {
	int p0 = 1;
	int p1 = 9;
	int p2 = 10;
	MinimalDifference * obj = new MinimalDifference();
	clock_t start = clock();
	int my_answer = obj->findNumber(p0, p1, p2);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p3 = 1;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p3 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p3 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
