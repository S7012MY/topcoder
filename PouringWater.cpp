int count(int n) {
	int r=0;
	do {
		++r;
	}while(n&=(n-1));
	return r;
}

struct PouringWater {
	int getMinBottles(int n, int k) {
		int rez=0;
		for(;count(rez+n)>k;++rez);
		return rez;
	}
};


double test0() {
	int p0 = 3;
	int p1 = 1;
	PouringWater * obj = new PouringWater();
	clock_t start = clock();
	int my_answer = obj->getMinBottles(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 1;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
