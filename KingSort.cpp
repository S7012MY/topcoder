	int ind[55],gnr[55];
	
	bool cmp(const int &i, const int &j) {
		int r= strcmp(aux[i].c_str(),aux[j].c_str());
		if(0==r) return gnr[i]<gnr[j];
		else return r!=1;
	}

struct KingSort {
	
	
	int conv(string s, int p) {
		int rez=0;
		int i;
		for(i=p; i<s.size()-1; ++i) {
			if(s[i]=='I') {
				if(s[i+1]=='V') rez+=4,++i;
				else if(s[i+1]=='X') rez+=9,++i;
				else if(s[i+1]=='L') rez+=49,++i;
				else ++rez;
			}else if(s[i]=='V') {
				if(s[i+1]=='L') rez+=45,++i;
				else rez+=5;
			}else if(s[i]=='X') {
				if(s[i+1]=='L') rez+=40,++i;
				else rez+=10;
			}else rez+=50;
		}
		if(i<s.size()) {
			if(s[i]=='I') ++rez;
			else if(s[i]=='V') rez+=5;
			else if(s[i]=='X') rez+=10;
			else rez+=50;
		}
		return rez;
	}
	
	vector <string> getSortedList(vector <string> kings) {
		//cout<<conv("IX",0);
		vector <string> rez;
		int n=kings.size();
		rez.resize(n);
		for(int i=0; i<n; ++i) {
			ind[i]=i;
			aux.push_back("");
			int j=0;
			for(;isalpha(kings[i][j]);aux[i]+=kings[i][j],++j);
			int nr=conv(kings[i],j+1);
			gnr[i]=nr;
		}
		sort(ind,ind+n,cmp);
		
		for(int i=0; i<n; ++i) rez[i]=kings[ind[i]];
		return rez;
	}
};


