int r[55];

struct TheSquareDivTwo {
	vector <string> solve(vector <string> board) {
		vector <string> rez=board;
		int n=board.size();
		for(int i=0; i<n; ++i) for(int j=0; j<n; ++j) {
			if('C'==board[i][j]) ++r[i];
			rez[i][j]='.';
		}
		for(int j=0; j<n; ++j) {
			for(int cnt=1,i=n-1;cnt<=r[j];++cnt,--i) rez[i][j]='C';
		}
		return rez;
	}
};


