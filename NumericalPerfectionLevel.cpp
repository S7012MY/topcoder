struct NumericalPerfectionLevel {
	int getLevel(long long N) {
		int nrd=0;
		for(LL f=2; f*f<=N; ++f)
			for(;0==N%f;N/=f,++nrd);
		if(N>1) ++nrd;
		if(nrd>=16) return 2;
		if(nrd>=4) return 1;
		
		return 0;
	}
};


double test0() {
	long long p0 = 4LL;
	NumericalPerfectionLevel * obj = new NumericalPerfectionLevel();
	clock_t start = clock();
	int my_answer = obj->getLevel(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 0;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
