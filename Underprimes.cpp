typedef set<int>::iterator is;

bitset<1000000> isp;
vector<int> pr;

struct Underprimes {
	int howMany(int A, int B) {
		int rez=0;
		isp[1]=1;
		for(int i=2; i<=B; ++i) if(!isp[i]) {
			pr.push_back(i);
			for(int j=i+i; j<=B; j+=i) isp[j]=1;
		}
		for(int i=A; i<=B; ++i) {
			int ci=i,cnt=0;
			for(int j=0; j<pr.size() && pr[i]*1LL*pr[i]<=ci; ++j) for(;0==ci%pr[j]; ++cnt,ci/=pr[j]);
			if(ci!=1) ++cnt;
			if(!isp[cnt]) {
				cout<<i<<' ';
				++rez;
			}
		}
		return rez;
	}
};


double test0() {
	int p0 = 2;
	int p1 = 10;
	Underprimes * obj = new Underprimes();
	clock_t start = clock();
	int my_answer = obj->howMany(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 5;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
