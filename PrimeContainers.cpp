struct PrimeContainers {
	int isp(int n) {
		if(n==1) return 0;
		for(int i=2; i*i<=n; ++i) if(0==n%i) return 0;
		return 1;
	}
	int containerSize(int N) {
		int rez=0;
		for(int i=1; i<N; i<<=1) {
			
			if(isp(N/i)) ++rez;
			//cout<<N/i<<' '<<i<<' '<<rez<<'\n';
		}
		return rez;
	}
};


double test0() {
	int p0 = 10;
	PrimeContainers * obj = new PrimeContainers();
	clock_t start = clock();
	int my_answer = obj->containerSize(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
