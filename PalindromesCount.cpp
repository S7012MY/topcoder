struct PalindromesCount {
	int count(string A, string B) {
		int rez=0;
		string c,rc;
		for(int i=0; i<A.size(); ++i) {
			c="";
			int pa=0;
			for(;pa<A.size(); ++pa) {
				if(pa==i) {
					for(int j=0; j<B.size(); ++j) c+=B[j];
				}
				c+=A[pa];
			}
			rc=c;
			reverse(rc.begin(),rc.end());
			if(c.compare(rc)==0) ++rez;
			//cout<<c<<' '<<rc<<'\n';
		}
		A.append(B);
		rc=A;
		reverse(rc.begin(),rc.end());
		if(rc.compare(A)==0) ++rez;
		return rez;
	}
};


